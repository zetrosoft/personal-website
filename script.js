// Typing animation for hero title
function typeWriter(element, text, speed = 100, onComplete = null) {
    let i = 0;
    
    function type() {
        if (i < text.length) {
            element.innerHTML += text.charAt(i);
            i++;
            setTimeout(type, speed);
        } else if (onComplete) {
            onComplete();
        }
    }
    
    type();
}

// Initialize typing animation when page loads
window.addEventListener('load', () => {
    const heroTitle = document.querySelector('.hero-title');
    if (heroTitle) {
        // Get the full text including the highlighted name
        const fullText = heroTitle.textContent;
        const highlightStart = fullText.indexOf('Iswan Putera');
        const textBefore = fullText.substring(0, highlightStart);
        const highlightText = 'Iswan Putera';
        const textAfter = '';
        
        // Clear the title
        heroTitle.innerHTML = '';
        
        // Type out the text before the highlight
        typeWriter(heroTitle, textBefore, 50, () => {
            // Create and append the highlight span
            const span = document.createElement('span');
            span.className = 'highlight';
            heroTitle.appendChild(span);
            
            // Type out the highlighted text
            typeWriter(span, highlightText, 30, () => {
                // Type any remaining text after the highlight
                if (textAfter) {
                    typeWriter(heroTitle, textAfter, 50);
                }
            });
        });
    }
});

